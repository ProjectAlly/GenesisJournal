		<p align="justify">
		<b>ABSTRACT:</b><br/>
            Bronchial asthma is well-known hypersensitivity disorder which prevalence is being rapidly intensified
            in present world particularly in developed countries. Since no adequate therapy is made available by
            modern medicine for its terminal and long lasting cure, complementary and alternative system of
            medicines are looked up for possibility by patients as well as physicians. Ayurveda being a major
            system of traditional medicines in the world, it cumulates abundant description on condition and
            therapy for asthma. Effort has been made to carry out overview about asthma therapy in Ayurveda view
            with possible comparative studies. Etiopathogenesis given by Ayurveda in ancient terms can be
            reasonably correlated with the modern findings. Therapeutically Ayurveda has used almost naturals to
            combat asthmatic condition as like other traditional system of medicines. But the way and sense to
            bring about use of naturals is extensively differing from the conventional approach of herbal usage.
            This unique sense of herbal usage by Ayurveda and how it proves advantageous to patients is discussed
            in present review. Ayurvedic herbal drugs, which have shown anti-asthmatic activity by modern
            findings, are summarized here to establish optimistic ethnopharmacological correlation. An attempt
            has also made to highlight other aspects of Ayurvedic therapy for clinical implementations of asthma
            either as independent or as integrated therapy approach.
		</p>
