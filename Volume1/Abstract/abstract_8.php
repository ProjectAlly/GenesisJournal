		<p align="justify">
		<b>ABSTRACT:</b><br/>
            Since long time, quinazoline and their derivatives are found to be active for the synthesis of various
            categories of drugs. The 2, 3-di-substituted quinazolin-4-(3H)-ones (III) and its derivatives have
            been synthesized as a one pot procedure from the reaction of Anthranilic acid (I), acid chlorides and
            different primary amines with the intermediate 4-(3H)-benzoxazinone (II) at different microwave
            conditions. High yield, less by products, short reaction time (7 to10 min), mild conditions and easy
            work-up are the advantages of this methodology. The pure compounds are separated through the
            preparative TLC method and using hydro-alcoholic solution. The final derivatives are characterized by
            UV, IR, Mass, NMR and screened for antimicrobial and antibacterial activity.
		</p>
		<p>
		<b>KEYWORDS:</b> Microwave assisted synthesis, one pot synthesis, 2, 3-di-substituted quinazolin-4-(3H)-ones, antimicrobial activity.
		
