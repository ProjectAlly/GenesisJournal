		<p align="justify">
		<b>ABSTRACT:</b><br/>
            A novel stability indicating HPLC assay method has been developed and validated for the estimation of
            rimonabant hydrochloride in tablet dosage form. A RP-HPLC separation was achieved on C18 column
            (250X4.6 mm i.d., 5 μm) utilizing a mobile phase comprising of Buffer: ACN in ratio of 35:65 mixed
            and the eluents from the column were monitored using a variable wavelength detector at 220 nm. The
            stress testing of rimonabant hydrochloride was carried out under acidic, alkaline hydrolysis,
            oxidation and thermal degradation (dry heat) conditions and rimonabant hydrochloride was well
            resolved from its degradation products with good resolution. The proposed method has permitted the
            quantification of rimonabant hydrochloride in the linearity range of 2-15 μg/ml and the flow rate
            was maintained at 1 ml/min. The retention times of rimonabant hydrochloride observed was  4.150 min.
            The method was validated as per ICH (International Conference on Harmonization) guidelines.
            From study, it was found that drug is susceptible for degradation to hydrolytic condition and
            oxidation but not affected by dry heat, UV light, thermal condition.
		</p>
		<p>
		<b>KEYWORDS:</b> Rimonabant, RP-HPLC, Stability-indicating assay, Forced degradation, Validation, ICH guidelines.
		
