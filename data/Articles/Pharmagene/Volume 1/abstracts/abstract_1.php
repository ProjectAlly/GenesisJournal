		<p align="justify">
		<b>ABSTRACT:</b><br/>	 
			 In the present study, the interaction of 4-(N-substituted)-3-pyridyl-5-mercapto-s-triazole
			 with secondary amines was explored. The Isonicotinic acid hydrazide was converted into 
			 the corresponding potassium dithiocarbazinate, by reacting with carbon disulphide in alkaline 
			 medium which undergoes ring closure reaction after further treatment with aqueous potassium 
			 hydroxide to give 5-pyridyl-2-mercapto-1,3,4-oxadiazole. 
			 4-(N-pyridylcarboxamido)-3- pyridyl-5-mercapto-s-triazole and 
			 3-pyridyl-4-amino-5-mercapto-s-triazole were obtained in one pot reaction by heating
			 equimolar quantities of oxadiazole with isonicotinic acid hydrazide and hydrazine hydrate respectively.
			 Condensation of triazole with secondary amines in anhydrous dimethyl sulfoxide results in the formation 
			 of corresponding quaternary salts. The synthesized compounds were confirmed by IR, 1NMR spectra
			 and elemental analysis. All the compounds were screened for their preliminary 
			 in-vitro antibacterial
			 and antifungal activity.
		</p>
		<p>
		<b>KEYWORDS:</b> 1,3,4-Oxadiazole, Quaternary Compound, Morpholine, Pyrrolidine.
		
