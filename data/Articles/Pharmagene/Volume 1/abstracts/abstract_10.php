		<p align="justify">
		<b>ABSTRACT:</b><br/>
            The objective of the present work is to develop and evaluate effervescent bioadhesive vaginal tablet
            of Itraconazole. The vaginal tablets were prepared by direct compression method. HPMC K4M, Sodium
            CMC, Cabopol 934P was used as bioadhesive polymers. Effervescent materials, sodium bicarbonate and
            citric acid was incorporated into the formulation because Itraconazole is a weak base with limited
            water solubility and it also improves the dissolution of bioadhesive vaginal tablets because there is
            rather low moisture content in vagina under normal physiological conditions. The amount of polymer
            blends and effervescent mixture was optimized using 32 full factorial design. The swelling and
            in-vitro release were studied. The ex-vivo bioadhesion was determined by using a physical modified
            balance. The ex-vivo mucoadhesion was determined by self developed modified mucoadhesion assembly.
            Anti-fungal activity of the effervescent bioadhesive tablet of Itraconazole was determined in
            comparison to drug solution by cup plate method. The effervescent Itraconazole tablet showed
            significant results (p<0.05) in all the four dependent factors. In-vitro antifungal activity was
            compared with pure drug solution and shown better antifungal activity due to sustained release of the
            drug. Thus our study may provide a potential vaginal formulation of Itraconazole against Vaginal
            candidiasis.
		</p>
