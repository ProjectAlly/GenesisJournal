		<p align="justify">
		<b>ABSTRACT:</b><br/>	 
		The aim of the present study was to evaluate in vitro cytotoxicity of Rubia cordifolia 
		against Hep G32 (human Hepatocellular carcinoma) cell line using XTT assay. Methanol 
		fraction of Rubia cordifolia extract exhibited potent inhibition of Hep G32 cell line 
		with IC50 value of 28.07 μg/ml while was found to be less cytotoxic against normal human 
		kidney cells with IC50 value more than 100 μg/ml displaying safety for normal cells. 		
