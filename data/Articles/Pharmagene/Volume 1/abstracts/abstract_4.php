		<p align="justify">
		<b>ABSTRACT:</b><br/>	 
		A precise and accurate reversed-phase high performance liquid  chromatographic (RP-HPLC) method has 
		been developed and subsequently validated for simultaneous estimation of Drospirenone  (DP) and 
		Ethinylestradiol (EE) from their combined pharmaceutical dosage form. The proposed RP-HPLC method 
		utilizes a Phenomenex Luna C18 (250mm x 4.6mm, 5 μm particle size) column at ambient temperature; 
		the optimum mobile phase consists of acetonitrile:water (60:40,v/v); mobile phase flow rate of 
		1.5 ml/min; and UV detection at 280 nm. The retention time for DP and EE were 2.7 and 5.3 minutes 
		respectively. The method was linear in the range of 80-400 μg/ml and 1.4-7.0 μg/ml and correlation 
		coefficients were 0.997 and 0.996 for DP and EE respectively. Proposed method was validated as per 
		ICH guidelines for precision, accuracy and linearity for estimation of DP and EE in commercially 
		available tablet dosage form. Results of the validation were found satisfactory.The proposed method 
		can be useful in the quality control of bulk manufacturing and pharmaceutical dosage forms. 		
		</p>
