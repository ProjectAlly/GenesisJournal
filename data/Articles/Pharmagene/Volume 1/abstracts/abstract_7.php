		<p align="justify">
		<b>ABSTRACT:</b><br/>
            A specific, accurate and precise spectrophotometric method using Folin Ciocalteu’s reagent for
            determination of moclobemide in tablet dosage form has been established and validated. Intense
            color was produced in the presence of 1.0 ml of 20% Na2CO3 solution and 3.0 ml of FC reagent
            solution (1:1 diluted with water) and wavelength maxima was 778 nm. Linearity over 10-200 µg/ml
            with r2 = 0.9982 was observed. %RSD for precision on replication was 0.1108 for seven replicate
            analyses. %RSD for intra-day and inter-day precision of moclobemide for 10-200 µg/ml was 0.128-0.495
            and 0.155-0.670, respectively. Detection limit (LOD) and quantification limit (LOQ) determined
            mathematically were 1.311µg/ml and 3.974µg/ml, respectively. % recovery was 98.578 – 100.269 ±
            0.245-0.612. Statistical analysis proves the method is repeatable and specific for analysis of
            moclobemide.
