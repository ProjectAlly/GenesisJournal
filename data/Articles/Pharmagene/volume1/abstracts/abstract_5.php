		<b>ABSTRACT:</b><br/>	 
		In the present investigation, series of 2-(4-methoxybenzyl) substituted-3-methyl-1,2 dihydropyrazol-5-one 
		and 2-(4-methoxybenzyl)-substituted-3,5-dimethyl-2,5-dihydro-1H-pyrazole have been synthesized. 
		All synthesized compound were characterized and were tested for anti-inflammatory and analgesic 
		activity by caraggenan induced rat paw edema method and hot plate method viz. It was interestingly 
		found that pyrazolone derivatives have shown lesser activity than that of the pyrazole derivatives. 
		Among all synthesized compounds compound 4a showed good activity.		
