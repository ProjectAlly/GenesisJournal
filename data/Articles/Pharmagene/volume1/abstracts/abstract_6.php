		<p align="justify">
		<b>ABSTRACT:</b><br/>	 
		Oxcarbazepine is an antiepileptic drug. It is a 10-keto analogue of carbamazepine with a similar therapeutic
		profile, but with less adverse effects and less clinical relevant pharmacokinetic drug interactions. This paper describes zer o
		and first order derivative spectrophotometric methods for determination of oxcarbazepine in pharmaceutical preparations. The
		solutions of the reference drug and pharmaceutical samples were prepared in methanol. Absorbance of oxcarbazepine was
		measured at 304.5 nm for zero order and at 266.5 nm for first order derivative spectrophotometric methods. Five brands were
		analysed and showed good results by both the methods. The linearity ranges were found to be 20-80 μg/ml and 10-100 μg/ml
		for zero and first order derivative spectrophotometric method respectively. The percentage recovery values of oxcarbazepine
		for both the methods were found between 98.57-101.02%. The precision (intraday, interday and repeatability) of the methods
		were found to be within limits. The methods developed in this study are accurate, sensitive, precise and reproducible and can
		be directly and easily applied to the pharmaceutical preparations.		
